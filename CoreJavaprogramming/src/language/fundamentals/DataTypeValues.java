package language.fundamentals;

public class DataTypeValues {
	
	public static void main(String[] args) {
		
		// Data Types Ranges
		char ch1='a';
		char ch2='A';
		System.out.println(ch1);
		System.out.println(ch2);
		
		
		for (int i =0;i<=200;i++) {
			char c =(char)i;
			System.out.println(c+" "+i);
		}
	}

}
